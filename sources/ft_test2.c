#include "../libft/libft.h"
#include "fillit.h"

int		ft_test2(char *str)
{
	int	i;

	i = 0;
	if (!*str)
		return (0);
	if (*str == '#' && *(str + 1) == '#')
		i++;
	if (*str == '#' && *(str + 5) == '#')
		i++;
	if (*str == '#' && *(str - 1) == '#')
		i++;
	if (*str == '#' && *(str - 5) == '#')
		i++;
	return (i + ft_test2(str + 1));
}
