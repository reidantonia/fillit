#include "libft.h"
#include "fillit.h"

int		ft_test2(char *str)
{
	if (!*str)
		return (0);
	else if (*str == '#' && (*(str + 1) == '#' || *(str - 1) == '#' \
				|| *(str + 5) == '#' || *(str - 5) == '#'))
		return (1 + (ft_test2(str + 1)));
	else
		return (ft_test2(str + 1));
}
